            <h3>3. Poisons, Opium, and Dangerous Drugs Ordinance</h3>
            <p>This law regulates drug-related offenses in Sri Lanka, including:</p>
            <ul>
                <li><strong>Possession of illegal drugs</strong> (e.g., heroin, cocaine, cannabis).</li>
            </ul>
            <ul>
                <li><strong>Drug trafficking</strong> (selling or distributing drugs).</li>
            </ul>
            <ul>
                <li><strong>Drug manufacturing and smuggling.</strong> The rights of accused persons and the steps in a criminal case.</li>
            </ul>
            <p>Punishments range from <strong>heavy fines to life imprisonment or the death penalty</strong> for large-scale drug trafficking.
            </p>
        </div>
        
        <div class="law-section">
            <h3>4. Prevention of Terrorism Act (PTA) (No. 48 of 1979)</h3>
            <p>This law is used to combat terrorism and extremist activities. It allows:</p>
            <ul>
                <li><strong>Extended detention:</strong> Police can hold a suspect for up to <strong> months without formal charges.</strong></li>
            </ul>
            <ul>
                <li><strong>Search and seizure:</strong> Authorities can search homes and seize property without a court order.</li>
            </ul>
            <ul>
                <li><strong>Restrictions on speech:</strong> Certain statements that promote terrorism can be considered crimes.</li>
            </ul>
            <p>The PTA has been controversial due to <strong> rights concerns</strong> and has faced international criticism.</p>
        </div>
        
        <div class="law-section">
            <h3>5. Computer Crimes Act (No. 24 of 2007)</h3>
            <p>This law punishes cybercrimes such as:</p>
            <ul>
                <li><strong>Hacking and unauthorized access to computer systems.</strong></li>
            </ul>
            <ul>
                <li><strong>Online identity theft and phishing scams.</strong></li>
            </ul>
            <ul>
                <li><strong>Cyberbullying and online harassment.</strong></li>
            </ul>
            <ul>
                <li><strong>Spreading fake news with criminal intent.</strong></li>
            </ul>
            <ul>
                <li><strong>Electronic fraud and credit card fraud.</strong></li>
            </ul>
        </div>
        
        <div class="law-section">
            <h3>6. Bribery Act (No. 11 of 1954)</h3>
            <p>This law addresses corruption and bribery, particularly among public officials. It includes:</p>
            <ul>
                <li><strong>Accepting or offering bribes.</strong></li>
            </ul>
            <ul>
                <li><strong>Misuse of public office for personal gain.</strong></li>
            </ul>
            <ul>
                <li><strong>Failure to report bribery.</strong></li>
            </ul>
            <p>The <strong> to Investigate Allegations of Bribery or Corruption (CIABOC)</strong> enforces this law.</p>
        </div>
        
        <div class="law-section">
            <h3>7. Domestic Violence Act (No. 34 of 2005)</h3>
            <p>This law protects victims of domestic violence, including:</p>
            <ul>
                <li><strong>Physical abuse</strong> (hitting, beating).</li>
            </ul>
            <ul>
                <li><strong>Verbal and emotional abuse</strong> (threats, intimidation).</li>
            </ul>
            <ul>
                <li><strong>Sexual abuse</strong> (forced intimacy).</li>
            </ul>
            <p>Victims can request Protection Orders to prevent the abuser from contacting them.</p>
        </div>
        
        <div class="law-section">
