        <div class="law-section">
            <h3>1. Penal Code of Sri Lanka (1883)</h3>
            <p>The Penal Code is the main criminal law of Sri Lanka. It defines different types of crimes and prescribes punishments for them.</p>
            <h4>A. Offenses Against the Person</h4>
            
                <ul><li><strong>Murder (Section 293-297):</strong> The intentional killing of a person. Punishable by death or life imprisonment.</li></ul>
                <ul><li><strong>Culpable Homicide (Section 298-304):</strong> Killing a person without premeditation. Lesser punishment than murder.</li></ul>
                <ul><li><strong>Assault (Section 341-342):</strong> Physically harming another person. Can lead to fines or imprisonment.</li></ul>
                <ul><li><strong>Rape (Section 363-364):</strong> Engaging in non-consensual sexual intercourse. Punishable by up to 20 years in prison.</li></ul>
                <ul><li><strong>Sexual Harassment (Section 345):</strong> Unwanted sexual advances, including verbal abuse, touching, or threats.</li></ul>
                <ul><li><strong>Kidnapping and Abduction (Section 355-358):</strong>  Illegally taking someone by force, fraud, or deception.</li></ul>
           
            <h4>B. Offenses Against Property</h4>
           
                <ul><li><strong>Theft (Section 366-369):</strong> Taking someone elseâ€™s property without permission. Punishable by fines or up to 3 years imprisonment.</li></ul>
                <ul><li><strong>Robbery (Section 390-392):</strong> Theft with force or violence. Punishable by up to 20 years imprisonment.</li></ul>
                <ul><li><strong>Criminal Breach of Trust (Section 388-389):</strong>  Misusing money or property entrusted to someone.</li></ul>
            
            <h4>C. Financial and Cyber Crimes</h4>
            
                <ul><li><strong>Forgery (Section 454-457):</strong> Creating false documents to cheat others.</li></ul>
                <ul><li><strong>Fraud and Cheating (Section 398-410):</strong> Deceiving someone for financial gain.</li></ul>
            
            <h4>D. Offenses Against the State</h4>
            
                <ul><li><strong>Treason (Section 114-120):</strong> Crimes against the government, such as attempting to overthrow it.</li></ul>
                <ul><li><strong>Sedition (Section 120):</strong> Encouraging rebellion against the government.</li></ul>
           
            <h4>E. Religious and Communal Offenses</h4>
            <ul>
                <li><strong>Inciting Religious or Ethnic Hatred (Section 291A, 291B):</strong> Promoting violence or discrimination based on religion or ethnicity.</li>
            </ul>
