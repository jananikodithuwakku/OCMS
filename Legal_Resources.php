            <p>This law defines how criminal cases should be handled. It includes:</p>
            <ul>
                <li><strong>Police powers:</strong> How and when police can arrest a suspect.</li>
            </ul>
            <ul>
                <li><strong>Filing complaints:</strong> How citizens can report crimes.</li>
            </ul>
            <ul>
                <li><strong>Bail system: </strong> How suspects can be released while awaiting trial.</li>
            </ul>
            <ul>
                <li><strong>Trial procedures:</strong> The rights of accused persons and the steps in a criminal case.</li>
            </ul>
